//Contador de caracteres: Escribir un programa que lea una palabra y presenta cuantos caracteres hay en dicha palabra.

// Pedir al usuario que ingrese una palabra
const userword = ("Ingresa una palabra:");

// Calcular la longitud (cantidad de caracteres) de la palabra
const wordlength = userword.length;

// Mostrar el resultado en pantalla
console.log(`La palabra "${userword}" tiene ${wordlength} caracteres.`);
