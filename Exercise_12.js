//Área de un triángulo: Indique la base y la altura de un triángulo, luego calcula y muestra su area

// Definimos las variables base y height.

var base = 10 ;
var height = 20 ;

// Calcular el área del triángulo usando la fórmula: Área = (base * altura) / 2

var area = (base * height) / 2 ;

//Mostrar en pantalla el area

console.log ("El area del triangulo es " + area ) ;

